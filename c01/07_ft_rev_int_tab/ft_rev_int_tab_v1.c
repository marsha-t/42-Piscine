void	ft_rev_int_tab(int *tab, int size)
{
	int	temp;
	int	i;
	int	mid;

	i = 0;
	mid = size / 2;
	while (i < mid)
	{
		temp = tab[i];
		tab[i] = tab[size - 1];
		tab[size - 1] = temp;
		i++;
		size--;
	}
}
