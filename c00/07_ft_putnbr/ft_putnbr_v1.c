#include <unistd.h>

// Approach 1
void	ft_putnbr(int nb)
{
	char	array[11];
	long	x;
	int		i;

	x = nb;
	i = 0;
	if (nb == 0)
		write(1, "0", 1);
	if (nb < 0)
	{
		x = -x;
		write(1, "-", 1);
	}
	while (x % 10 != 0 || (x > 0 && x % 10 == 0))
	{
		array[i++] = (x % 10) + '0';
		x = x / 10;
	}
	while (i >= 0)
	{
		i--;
		write(1, &array[i], 1);
	}
}
