#include <unistd.h>

void	ft_putnbr(int nb)
{
	char 		*base; 
	long long	x;

	base = "0123456789";
	x = nb;
	if (x < 0)
	{
		write(1, "-", 1);
		x = -x;
	}
	if (x >= 10)
		ft_putnbr(x / 10);
	write(1, &base[x % 10], 1);
}

