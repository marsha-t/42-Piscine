#include <unistd.h>

void	ft_putnbr(int nb)
{
	char 		base[11] = "0123456789";
	long long	x;
	x = nb;
	if (x < 0)
	{
		write(1, "-", 1);
		x = -x;
	}
	if (x > 9)
		ft_putnbr(x / 10);
	write(1, &base[x % 10], 1);
}

