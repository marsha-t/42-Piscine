#include <unistd.h>

// Approach 2 - using recursion
void	ft_putnbr(int nb)
{
	char c;
	if (nb < 0)
	{
		write(1, "-", 1);
	}
	if (nb > 9)
		ft_putnbr(nb / 10);
	c = nb % 10 + '0';
	write(1, &c, 1);
}
