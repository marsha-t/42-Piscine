int	ft_str_is_lowercase(char *str)
{
	int	i;
	int	r;

	i = 0;
	r = 1;
	while ((str[i] != '\0') && (r == 1))
	{
		if (((str[i] >= 'a') && (str[i] <= 'z')))
			str++;
		else
			r = 0;
	}
	return (r);
}
